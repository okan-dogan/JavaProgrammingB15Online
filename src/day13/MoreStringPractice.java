package day13;

public class MoreStringPractice {

    public static void main(String[] args) {

        String name = "Pumpkin";

        System.out.println(name);

        name.toUpperCase();
        System.out.println(name);

        System.out.println(name.toUpperCase());

        name = name.toUpperCase();
        System.out.println(name);
    }
}
