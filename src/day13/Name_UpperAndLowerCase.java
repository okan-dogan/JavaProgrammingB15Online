package day13;

public class Name_UpperAndLowerCase {

    public static void main(String[] args) {

        String myName = new String("Okan");
        System.out.println("MY NAME IS : "+myName.toUpperCase());
        System.out.println("my name is : "+myName.toLowerCase());
        System.out.println("How many characters my name has? : "+myName.length());

    }
}
