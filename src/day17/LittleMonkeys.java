package day17;

public class LittleMonkeys {

    public static void main(String[] args) {

        for (int i = 5; i >0 ; i--) {

            System.out.println(i+ " little monkeys jumping on the bed,\nOne fell down and bumped his head,\n" +
                    "Mama called the doctor and the doctor said,\nNo more monkeys jumping on the bed!");
            System.out.println();

        }
        System.out.println("Put those monkeys right to bed!");

    }
}


