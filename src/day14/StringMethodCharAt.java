package day14;

public class StringMethodCharAt {

    public static void main(String[] args) {

        // charAt method of String
        // it will return a character at certain location specified

        String name = "Okan Dogan";
        System.out.println(name);

        char c0 = name.charAt(0);
        System.out.println(c0);
        char c1 = name.charAt(1);
        System.out.println(c1);
        char c2 = name.charAt(2);
        System.out.println(c2);
        char c3 = name.charAt(3);
        System.out.println(c3);
        char c4 = name.charAt(4);
        System.out.println(c4);
        char c5 = name.charAt(5);
        System.out.println(c5);
        char c6 = name.charAt(6);
        System.out.println(c6);
        char c7 = name.charAt(7);
        System.out.println(c7);
        char c8 = name.charAt(8);
        System.out.println(c8);
        char c9 = name.charAt(9);
        System.out.println(c9);

        System.out.print(name.charAt(0)); // not ..println, but ..print to get all the letters in one line
        System.out.print(name.charAt(1));
        System.out.print(name.charAt(2));
        System.out.print(name.charAt(3));
        System.out.print(name.charAt(4));
        System.out.print(name.charAt(5));
        System.out.print(name.charAt(6));
        System.out.print(name.charAt(7));
        System.out.print(name.charAt(8));
        System.out.print(name.charAt(9));

    }
}
