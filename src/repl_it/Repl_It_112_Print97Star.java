package repl_it;
/*
Given an int variable k that has already been declared,
use a for loop to print a single line consisting of 97 asterisks. Use no variables other than k.
Need No Array Just Loop
 */
public class Repl_It_112_Print97Star {

    public static void main(String[] args) {

        int k = 1;

        for (k = 1; k <=97 ; k++) {
            System.out.print("*");
        }

    }
}
